    @Override
    public void newData(List<GetAvailibleServices.Service> services, List<GetAvailibleRooms.Room> rooms) {
        mGroup.rooms.clear();
        mGroup.rooms.addAll(rooms);
        mGroup.services.clear();
        mGroup.services.addAll(services);

    }

    @Override
    public void update() {
        int pos = -1;
        for (int i = 0; i < mParent.Orders.size(); i++) {
            if (mParent.Orders.get(i).contains(mGroup)) {
                pos = i;
                break;
            }
        }
        this.current_pos = pos;
        indexes = mParent.getAvailibleOrders(false, mGroup);
        String[] labels = new String[indexes.size()];
        for (int i = 0; i < indexes.size(); i++) {
            labels[i] = Procedure.Order_Labels[indexes.get(i)];
        }

        DefaultComboBoxModel<String> t = new DefaultComboBoxModel<String>(labels);
        order_list.setModel(t);
        order_list.setSelectedIndex(indexes.indexOf(pos));
        mGroup.order = pos;
    }

