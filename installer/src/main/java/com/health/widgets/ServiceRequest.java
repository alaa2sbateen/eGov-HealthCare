    public void setData(GetProcedure.Procedure procedure, GetProcedure.ServicesGroup group, Procedure parent) {
        mProcedure = procedure;
        mGroup = group;
        mParent = parent;
        mParent.registerListener(this);

        CountSpinner.setValue(group.count);
        note.setText(group.note_med);

        System.out.println("----data ");
        System.out.println(group.rooms);
        System.out.println(group.services);

        for (int i = 0; i < group.services.size(); i++) {
            List<Object> objs = new ArrayList<>();
            Util.getAllObjects((DefaultMutableTreeNode) rooms_matcher.Services.getModel().getRoot(), objs);
            for (Object d : objs) {
                if (d instanceof GetAvailibleServices.Service) {
                    if (((GetAvailibleServices.Service) d).service_id == group.services.get(i).service_id) {
                        rooms_matcher.Services.toggleNode(Util.findNode((DefaultMutableTreeNode) rooms_matcher.Services.getModel().getRoot(), d));
                    }
                }
            }
        }
        for (int i = 0; i < group.rooms.size(); i++) {
            List<Object> objs = new ArrayList<>();
            Util.getAllObjects((DefaultMutableTreeNode) rooms_matcher.Rooms.getModel().getRoot(), objs);
            for (Object d : objs) {
                if (d instanceof GetAvailibleRooms.Room) {
                    if (((GetAvailibleRooms.Room) d).room_id == group.rooms.get(i).room_id) {
                        rooms_matcher.Rooms.toggleNode(Util.findNode((DefaultMutableTreeNode) rooms_matcher.Rooms.getModel().getRoot(), d));
                    }
                }
            }
        }

    }

